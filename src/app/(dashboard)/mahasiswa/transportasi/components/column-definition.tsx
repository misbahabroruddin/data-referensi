"use client";

import { ColumnDef } from "@tanstack/react-table";

import { DeleteIcon } from "@/components/svg/delete";
import { Spinner } from "@/components/ui/spinner";
import { ModalDetailTransportasi } from "./modal-detail-transportasi";
import { useDeleteTransportasi } from "@/handlers/mahasiswa/transportasi/delete-transportasi";

export const useColumnTable = () => {
  const { mutateAsync, isPending: isLoadingDelete } = useDeleteTransportasi();

  const columns: ColumnDef<Transportasi>[] = [
    {
      accessorKey: "id",
      header: () => <div className="text-center">No</div>,
      cell: ({ row }) => {
        return <div className="text-center">{row.index + 1}</div>;
      },
    },
    {
      accessorKey: "transportasi",
      header: () => {
        return <div className="text-center">Transportasi</div>;
      },
      cell: ({ row }) => (
        <div className="text-start">{row.original.transportasi}</div>
      ),
    },
    {
      accessorKey: "kode",
      header: () => {
        return <div className="text-center">Kode</div>;
      },
      cell: ({ row }) => <div className="text-start">{row.original.kode}</div>,
    },
    {
      accessorKey: "id",
      header: () => <div className="text-center">Action</div>,
      cell: ({ row }) => (
        <div className="flex items-center justify-center gap-1">
          <ModalDetailTransportasi transportasiId={row.original.id!} isEdit />
          <ModalDetailTransportasi transportasiId={row.original.id!} />
          <button
            title="Hapus"
            onClick={() => mutateAsync(row.original.id!)}
            disabled={isLoadingDelete}
          >
            {isLoadingDelete ? (
              <Spinner className="h-5 w-5" />
            ) : (
              <DeleteIcon className="ml-[6px]" height="15" width="14" />
            )}
          </button>
        </div>
      ),
    },
  ];

  return { columns };
};
